import java.util.Scanner;

public class Solution {
    
    public static void main(String[] args) {
    
        Scanner s=new Scanner(System.in);
        
        String str=s.nextLine();
        char c=str.charAt(0);
        if(c>=97 && c<=127)
            System.out.println("0");
        else if(c>=65 && c<=90)
            System.out.println("1");
        else if(c>0)
            System.out.println("-1");
    }
}
